// 1. Number Type
let num1 = 27;
let num2 = 1.5;
let num3 = -20;

console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);
console.log(num1 % num2);

let inf = Infinity;
let mInf = -Infinity;

let nan = NaN;

console.log(1 * 'hello');

// 2. String Type
let myName = 'Rin';
let myLocation = 'Seoul';
let introduce = myName + myLocation;
console.log(introduce);

let introduceText = `${myName}은 ${myLocation}에 거주합니다.`;
console.log(introduceText);

// 3. Boolean Type
let isSwitchOn = true;
let isEmpty = false;

// 4. Null Type ("아무것도 없음"을 할당)
let empty = null;

// 5. Undefined Type (아무것도 할당하지 않음)
let none;
console.log(none);
